import re

Token = tuple[str, float | None]  # токен для парсинга

TOKEN_RE = re.compile(  # шаблон парсинга для токенизации
    r"""
    \s*(
        [+-]?\d+(?:\.\d+)?
        | \*\* 
        | // 
        | [%()+\-*/]
    )""",
    re.VERBOSE,
)


HIERARCHY = {
    "(": 0,
    ")": 0,
    "+": 1,
    "-": 1,
    "*": 2,
    "/": 2,
    "//": 2,
    "%": 2,
    "**": 3,
}  # иерархия для перевода в польскую запись
